package variables;

public class VariableIntro {
    /**
     * Types of Variables:
     *
     * float
     * char
     * Integer
     * Double
     * String
     * Boolean
     * */


    //Primitive And NOn-Primitive Variables
    //Primitive variables are variables that cannot hold null and are registered as bits in the memory
    //Examples int, boolean, char, float, double

    //Non-Primitive Are wrapper class
    //Example: String, Integer, Double, Float, Boolean

    //Primitives
    int age = 20;
    boolean isMarried = true;
    char gender = 'M';
    float weight = 35.6f;
    double length = 3.142;

    //Non-Primitive
    String name = "Ikechukwu";
    String stillThinking = null;

    Integer money = 2500;
    Integer noMoney = null;

    Boolean isValid = null;

}
